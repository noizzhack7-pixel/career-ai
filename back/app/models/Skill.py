import uuid
from typing import Literal, Union

from pydantic import BaseModel, Field
from app.models.BaseValues import SkillRange, SoftSkills, HardSkills


class SoftSkill(BaseModel):
    """A soft skill with a validated proficiency level."""

    skill: Literal[SoftSkills]
    level: SkillRange
    id: str = Field(default_factory=uuid.uuid4, description="Unique skill identifier, is generated by default.")

    class Config:
        frozen = True  # Skills should be immutable once created
        json_schema_extra = {
            "example": {
                "skill": "Communication",
                "level": 4.5,
            }
        }


class HardSkill(BaseModel):
    """A hard skill with a validated proficiency level."""

    skill: Literal[HardSkills]
    level: SkillRange
    id: str = Field(default_factory=uuid.uuid4, description="Unique skill identifier, is generated by default.")

    class Config:
        frozen = True  # Skills should be immutable once created
        json_schema_extra = {
            "example": {
                "skill": "Communication",
                "level": 4.5,
            }
        }


# Union type for any skill
Skill = Union[SoftSkill, HardSkill]
